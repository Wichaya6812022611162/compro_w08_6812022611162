#include <stdio.h>

int main (){
    int i = 2 ;
    for ( int i = 2 ; i <= 10 ; i+=2 ) {
        printf ("Round %d \n", i ) ;
    }

    while (i<= 10) {
        printf ("Round %d \n", i ) ;
        i+=2 ;

    }
    printf ("End of loop \n") ;

    return 0 ;

}
/*ทั้ง 2โค้กทำงานเหมือนกัน แสดงเลขคู่ จาก2 ถึง 10 ออกมา โดยสร้างการควบคุมการทำซ้ำที่แตกต่างกัน
ผลลัพธ์ที่ได้จาก ทัั้ง 2โค้ดจะเหมือนกัน คือการแสดงเลขคู่ จาก 2 ถึง 10 ออกมา และปิดท้าย ด้วย "End of loop*/